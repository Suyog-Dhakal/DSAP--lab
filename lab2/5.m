plot(t,y3,'LineWidth',2);
title('y_3(t)=sin(3*w0*t)');
y4=sin(4*w0*t);
subplot(3,2,4);
plot(t,y4,'LineWidth',2);
title('y_4(t)=sin(4*w0*t)');
